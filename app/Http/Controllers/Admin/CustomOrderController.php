    {
        $order = CustomOrder::find($id);

        $status = $order->status;

        if($status == 0){
            $order->status = 1;
            $order->save;
        }

        $message = $order->status ? 'Order Declined' : 'Account Declined';

        return back()->withInput()->with('success', $message);
    }

